# Common suffixes that bloat slug names when they come from verbose registry
# project titles. Stripped BEFORE slugification so that "Super Nyadi
# Hydropower Project" → "super-nyadi" rather than "super-nyadi-hydropower-project".
PROJECT_SUFFIXES = [
    " Hydropower Project", " Hydroelectric Project", " Hydro-Electric Project",
    " Hydropower Project cascade project",
    "HEP", "HPP", "SHP", "HP",
]

def clean_project_name(name: str) -> str:
    """Strip verbose registry suffixes from project names for cleaner slugs."""
    result = (name or "").strip()
    for suffix in PROJECT_SUFFIXES:
        if result.endswith(suffix) and len(result) - len(suffix) >= 3:
            result = result[: -len(suffix)].rstrip()
            break
    return result

def slugify(name: str) -> str:
    """Matches explorer's slug rule for map 'would-be slug'."""
    return re.sub(r"[^a-z0-9]+", "-", (name or "").lower()).strip("-")


def load_features(manifest: dict, layer_key: str) -> list[dict]:
    layer = manifest["layers"][layer_key]
    path = (SHARED / layer["path"]).resolve()
    gj = json.loads(path.read_text(encoding="utf-8"))
    feats = gj.get("features", [])
    flt = layer.get("filter")
    if flt:
        feats = [ft for ft in feats if (ft.get("properties") or {}).get(flt["field"]) == flt["equals"]]
    return feats


def feature_name(layer_key: str, props: dict) -> str | None:
    """Pick the canonical project name for a feature. hydropower layers use
    'project'; curated layers (watchlist/storage/top) prefer 'project' too,
    fall back to 'label_title'/'name'."""
    for k in ("project", "label_title", "name"):
        v = props.get(k)
        if v:
            return str(v)
    return None


def guess_basin(river: str | None, basin_hint: str | None) -> str | None:
    # Try explicit basin hint first (covers storage-shortlist where 'basin' is set).
    for s in filter(None, (basin_hint, river)):
        haystack = str(s).strip().lower()
        if "koshi" in haystack:   return "koshi-basin"
        if "gandak" in haystack:  return "gandaki-basin"
        if "karnali" in haystack: return "karnali-basin"
        if "mahakali" in haystack or "chameliya" in haystack or "chamliya" in haystack: return "mahakali-basin"
        for needle, slug in BASIN_BY_RIVER:
            if needle in haystack:
                return slug
    return None


def titlecase(name: str) -> str:
    """Light title-casing that preserves acronyms like HEP/HPP/MW and roman numerals."""
    if not name:
        return name
    keep = {"HEP", "HPP", "MW", "I", "II", "III", "IV", "V", "VI", "A", "B"}
    parts = re.split(r"(\s+|-)", name)
    out = []
    for p in parts:
        if p.upper() in keep:
            out.append(p.upper())
        elif p.strip() == "" or p == "-":
            out.append(p)
        else:
            out.append(p[:1].upper() + p[1:])
    return "".join(out)


def fmt_capacity(cap: float | int | None) -> str | None:
    if cap is None or cap == "":
        return None
    try:
        f = float(cap)
        if f == int(f):
            return f"{int(f)} MW"
        return f"{f:g} MW"
    except (TypeError, ValueError):
        return None


def license_human(lt: str | None) -> str:
    m = {"Operation": "Operating", "Generation": "Under construction (Generation licence)", "Survey": "Survey / study"}
    return m.get(lt or "", lt or "—")


def build_tags(layers_in: set[str], props: dict, basin: str | None) -> list[str]:
    tags = ["project"]
    if "priority_watchlist" in layers_in: tags.append("watchlist")
    if "storage_shortlist"  in layers_in: tags.append("storage")
    if "top_capacity"       in layers_in: tags.append("top-capacity")
    lt = (props.get("license_type") or "").lower()
    if lt == "operation":   tags.append("operating")
    elif lt == "generation":tags.append("under-construction")
    elif lt == "survey":    tags.append("survey")
    if basin:
        tags.append(basin.replace("-basin", ""))
    if props.get("district"):
        tags.append(props["district"].lower())
    # dedupe + order
    seen, out = set(), []
    for t in tags:
        if t and t not in seen:
            seen.add(t); out.append(t)
    return out


