def existing_slug_set() -> set[str]:
    out: set[str] = set()
    for cat in ("entities", "concepts", "syntheses", "claims", "sources", "data"):
        d = PAGES / cat
        if d.is_dir():
            for f in d.glob("*.md"):
                out.add(f.stem)
    return out


def is_auto_stub(md_path: Path) -> bool:
    try:
        text = md_path.read_text(encoding="utf-8")
    except OSError:
        return False
    if not text.startswith("---"):
        return False
    end = text.find("\n---", 4)
    if end == -1:
        return False
    return re.search(r"^generator:\s*(auto-stub|specs-refresh)\s*$", text[4:end], re.MULTILINE) is not None


def refresh_spec_block(md_path: Path, entry: dict, specs_lookup: dict[str, dict[str, str]] | None = None) -> bool:
    text = md_path.read_text(encoding="utf-8")
    new_block = render_spec_block(entry, specs_lookup)
    if SPEC_START in text and SPEC_END in text:
        pattern = re.compile(re.escape(SPEC_START) + r".*?" + re.escape(SPEC_END), re.DOTALL)
        new_text = pattern.sub(new_block, text, count=1)
        # also bump `updated:` in frontmatter if present
        new_text = re.sub(r"^updated:\s*.+$", f"updated: {dt.date.today().isoformat()}", new_text, count=1, flags=re.MULTILINE)
        if new_text != text:
            md_path.write_text(new_text, encoding="utf-8")
            return True
    return False


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--write", action="store_true", help="Actually write files (default: dry-run)")
    ap.add_argument("--refresh-specs", action="store_true", help="Also refresh generated spec blocks in existing auto-stubs")
    args = ap.parse_args()

    manifest = json.loads(MANIFEST.read_text(encoding="utf-8"))
    bindings = json.loads(BINDINGS.read_text(encoding="utf-8"))
    existing = existing_slug_set()
    targets = collect_targets(manifest)
    specs_lookup = load_specs_csv()

    print(f"collected {len(targets)} target slugs across {len(TARGETS)} layers")
    print(f"existing wiki pages: {len(existing)}")
    print(f"existing bindings:   {len(bindings.get('pages', {}))}")
    if specs_lookup:
        print(f"specs CSV entries:   {len(specs_lookup)}")

    # Classify
    to_create, to_skip_alias, to_refresh, already_covered = [], [], [], []
    for slug, entry in sorted(targets.items()):
        aliased = SLUG_ALIASES.get(slug)
        if aliased and aliased in existing:
            to_skip_alias.append((slug, aliased)); continue
        if slug in existing:
            path = ENTITIES / f"{slug}.md"
            if is_auto_stub(path):
                to_refresh.append(slug)
            else:
                already_covered.append(slug)
            continue
        to_create.append(slug)

    print()
    print(f"would create:      {len(to_create):4d}")
    print(f"already have page: {len(already_covered):4d}  (hand-written, untouched)")
    print(f"auto-stub (refreshable): {len(to_refresh):4d}")
    print(f"aliased to existing:     {len(to_skip_alias):4d}")
    print()

    if args.write or args.refresh_specs:
        ENTITIES.mkdir(parents=True, exist_ok=True)

    # Create new stubs
    created_count = 0
    if to_create and args.write:
        for slug in to_create:
            path = ENTITIES / f"{slug}.md"
            md = render_stub(slug, targets[slug], specs_lookup)
            path.write_text(md, encoding="utf-8")
            created_count += 1
        print(f"wrote {created_count} new stubs -> wiki/pages/entities/")
    elif to_create:
        print(f"[dry-run] would write {len(to_create)} new stubs. Sample:")
        for slug in to_create[:8]:
            print(f"   - {slug}.md  ({titlecase(targets[slug]['name'])})")
        if len(to_create) > 8: print(f"   ... +{len(to_create)-8} more")

    # Refresh spec blocks in existing auto-stubs
    if args.refresh_specs and to_refresh:
        refreshed = 0
        for slug in to_refresh:
            if refresh_spec_block(ENTITIES / f"{slug}.md", targets[slug], specs_lookup):
                refreshed += 1
        print(f"refreshed spec block in {refreshed}/{len(to_refresh)} auto-stubs")

    # Update bindings (only for files that now exist)
    existing_after = existing_slug_set() if args.write else (existing | set(to_create))
    stats = sync_bindings(bindings, targets, existing_after, write=args.write)
    print(f"bindings: {stats['added']} new (+{stats['aliased_added']} aliased), {stats['already']} already present")
    if not args.write:
        print("\n[dry-run] rerun with --write to apply.")
        return 0

    # Rebuild the static indexes the explorer reads. Order matters:
    # backlinks depend on the page index, so run the index builder first.
    for script in (
        "build_wiki_page_index.py",
        "build_wiki_page_meta.py",
        "build_backlinks.py",
    ):
        print(f"--> {script}")
        subprocess.run([sys.executable, str(ROOT / "scripts" / script)], check=True, cwd=ROOT)
    return 0


if __name__ == "__main__":
    sys.exit(main())
