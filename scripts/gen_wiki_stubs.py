def collect_targets(manifest: dict) -> dict[str, dict]:
    """Return: { slug: {
            'name', 'props', 'layers': set[str], 'ids': {layer: [ids]},
            'pri_rank': int, 'source_notes': [str], 'rivers': set, 'basins': set,
        } }
    Merged across every layer the project appears in, with lower-rank layers'
    props winning (watchlist label_title beats a survey-layer typo).
    """
    bundle: dict[str, dict] = {}
    for layer_key, rank, floor in TARGETS:
        feats = load_features(manifest, layer_key)
        for ft in feats:
            p = dict(ft.get("properties") or {})
            name = feature_name(layer_key, p)
            if not name:
                continue
            if floor is not None:
                cap = p.get("capacity_mw") or p.get("installed_mw") or 0
                try:
                    if float(cap) < floor:
                        continue
                except (TypeError, ValueError):
                    continue
            slug = slugify(clean_project_name(name))
            if not slug:
                continue
            entry = bundle.setdefault(slug, {
                "name": name, "props": {}, "layers": set(), "ids": {},
                "pri_rank": rank, "rivers": set(), "basins": set(),
                "source_notes": [],
            })
            entry["layers"].add(layer_key)
            entry["pri_rank"] = min(entry["pri_rank"], rank)
            # merge props: lower rank wins per-key
            for k, v in p.items():
                if v in (None, ""):
                    continue
                existing_rank = entry.setdefault("_rank_for", {}).get(k, 99)
                if rank < existing_rank:
                    entry["props"][k] = v
                    entry["_rank_for"][k] = rank
                elif k not in entry["props"]:
                    entry["props"][k] = v
                    entry["_rank_for"][k] = rank
            fid = p.get("id")
            if fid:
                entry["ids"].setdefault(layer_key, []).append(fid)
            if p.get("river"):   entry["rivers"].add(p["river"])
            if p.get("basin"):   entry["basins"].add(p["basin"])
            if p.get("source_note"):
                entry["source_notes"].append(p["source_note"])
            # use the canonical (lowest-rank) layer's name as display name
            if rank <= entry["pri_rank"]:
                entry["name"] = name
    return bundle


