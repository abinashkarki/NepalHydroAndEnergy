def load_specs_csv() -> dict[str, dict[str, str]]:
    """Read the project specs CSV and return {slug: {column: value}}."""
    import csv as csv_module
    if not SPECS_CSV.exists():
        return {}
    lookup: dict[str, dict[str, str]] = {}
    with SPECS_CSV.open(newline="", encoding="utf-8-sig") as f:
        reader = csv_module.DictReader(f)
        for row in reader:
            slug = (row.get("slug") or "").strip()
            if slug:
                # Filter out None keys (artifacts from trailing commas) and empty values.
                lookup[slug] = {
                    k.strip(): str(v).strip()
                    for k, v in row.items()
                    if k is not None and k.strip() and str(v).strip()
                }
    return lookup


def _render_sectioned_specs(slug: str, specs_lookup: dict[str, dict[str, str]]) -> str | None:
    """Return sectioned spec sub-tables for a slug, or None if no specs exist."""
    if not specs_lookup or slug not in specs_lookup:
        return None
    row = specs_lookup[slug]
    blocks: list[str] = []
    for section_name, fields in SPEC_SECTIONS:
        rows_out: list[str] = []
        for col, label, suffix in fields:
            val = row.get(col)
            if val is None or val == "":
                continue
            # Format display value
            display = val
            try:
                if suffix and "." in val:
                    # Check if it looks like a number with decimal
                    float(val)
            except ValueError:
                pass
            if suffix:
                display = f"{val}{suffix}"
            rows_out.append(f"| {label} | {display} |")
        if rows_out:
            blocks.append(f"### {section_name}\n\n| Parameter | Value |\n|-----------|-------|\n" + "\n".join(rows_out))
    return "\n\n".join(blocks) if blocks else None


def render_spec_block(entry: dict, specs_lookup: dict[str, dict[str, str]] | None = None) -> str:
    p = entry["props"]
    rows: list[tuple[str, str]] = []
    cap = fmt_capacity(p.get("capacity_mw") or p.get("installed_mw"))
    if cap: rows.append(("Capacity", cap))
    if p.get("license_type"):
        rows.append(("Status", license_human(p.get("license_type"))))
    if "storage_shortlist" in entry["layers"] and p.get("category"):
        rows.append(("Category", str(p["category"]).replace("_", " ")))
    if p.get("river"):
        basin = guess_basin(p.get("river"), p.get("basin"))
        river_cell = p["river"]
        if basin:
            river_cell = f"{p['river']} (eastern [[{basin}]])" if basin == "koshi-basin" else f"{p['river']} ([[{basin}]])"
        rows.append(("River", river_cell))
    if p.get("basin") and not p.get("river"):
        basin = guess_basin(None, p.get("basin"))
        rows.append(("Basin", f"{p['basin']} ([[{basin}]])" if basin else str(p["basin"])))
    if p.get("district"):
        rows.append(("District", p["district"].title() if p["district"].isupper() else p["district"]))
    if p.get("province"):
        rows.append(("Province", p["province"]))
    if p.get("annual_energy_gwh"):
        rows.append(("Annual energy", f"{p['annual_energy_gwh']} GWh"))
    if p.get("dry_energy_gwh"):
        rows.append(("Dry-season energy", f"{p['dry_energy_gwh']} GWh"))
    if p.get("dry_share_pct"):
        rows.append(("Dry share", f"{p['dry_share_pct']}%"))
    if p.get("total_storage_mcm"):
        rows.append(("Total storage", f"{p['total_storage_mcm']} MCM"))
    if p.get("effective_storage_mcm"):
        rows.append(("Effective storage", f"{p['effective_storage_mcm']} MCM"))
    if p.get("promoter"):
        rows.append(("Promoter", p["promoter"]))

    lines = [SPEC_START, "", "## Specifications", "", "| Parameter | Value |", "|-----------|-------|"]
    for k, v in rows:
        lines.append(f"| {k} | {v} |")
    lines.append("")

    # Append sectioned specs from the project_specs.csv if available.
    slug = slugify(clean_project_name(entry["name"]))
    sectioned = _render_sectioned_specs(slug, specs_lookup)
    if sectioned:
        lines.append(sectioned)
        lines.append("")

    lines.append(SPEC_END)
    return "\n".join(lines)


def render_stub(slug: str, entry: dict, specs_lookup: dict[str, dict[str, str]] | None = None) -> str:
    p = entry["props"]
    title = titlecase(entry["name"])
    today = dt.date.today().isoformat()
    basin = guess_basin(p.get("river"), p.get("basin"))
    tags = build_tags(entry["layers"], p, basin)

    see_also: list[str] = []
    if basin: see_also.append(basin)
    lt = (p.get("license_type") or "").lower()
    if lt == "operation":
        see_also.append("run-of-river-hydropower")
    if "storage_shortlist" in entry["layers"]:
        see_also += ["storage-deficit", "firm-power"]
    if "priority_watchlist" in entry["layers"]:
        see_also.append("stranded-generation")
    if lt == "survey":
        see_also.append("buildability")
    # dedupe, keep order
    sa_seen, sa_ordered = set(), []
    for s in see_also:
        if s not in sa_seen:
            sa_seen.add(s); sa_ordered.append(s)

    lead_bits = []
    cap = fmt_capacity(p.get("capacity_mw") or p.get("installed_mw"))
    if cap: lead_bits.append(cap)
    lt = p.get("license_type")
    if lt:
        lead_bits.append(license_human(lt).lower())
    elif "storage_shortlist" in entry["layers"]:
        cat = str(p.get("category") or "").replace("_", " ").strip()
        lead_bits.append(cat or "storage candidate")
    if p.get("river"):    lead_bits.append(f"on the {p['river']}")
    elif p.get("basin"):  lead_bits.append(f"in the {p['basin']} basin")
    if p.get("district"): lead_bits.append(f"in {str(p['district']).title() if p['district'].isupper() else p['district']} district")
    lead = " · ".join(lead_bits).rstrip(".") + "."

    # frontmatter — uses block list for images so they're easy to hand-fill.
    fm_lines = [
        "---",
        f'title: {title}',
        "type: entity",
        f"created: {today}",
        f"updated: {today}",
        "sources: []",
        f"tags: [{', '.join(tags)}]",
        "images: []",
        "generator: auto-stub",
        "---",
        "",
        f"# {title}",
        "",
        lead,
        "",
        render_spec_block(entry, specs_lookup),
        "",
        "## Notes",
        "",
        "> [!note] This is a registry-backed project record. Capacity, location,",
        "> and licence status come from the Ministry of Energy registry",
        "> mirrored in the map data. Narrative context and images are added",
        "> where public sources are strong enough; the specification table is",
        "> maintained from the registry.",
        "",
    ]

    if entry["source_notes"]:
        notes = sorted({str(n).strip() for n in entry["source_notes"] if n})
        if notes:
            fm_lines += ["## Registry note", ""]
            for n in notes:
                fm_lines.append(f"- {n}")
            fm_lines.append("")

    if sa_ordered:
        fm_lines += ["## See also", ""]
        for s in sa_ordered:
            fm_lines.append(f"- [[{s}]]")
        fm_lines.append("")

    return "\n".join(fm_lines)


