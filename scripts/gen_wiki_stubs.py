def build_binding_features(entry: dict, canonical_slug: str) -> list[dict]:
    """Shape the feature list the same way existing bindings look."""
    feats: list[dict] = []
    primary_layer = min(entry["layers"], key=lambda k: next(r for _k, r, _ in TARGETS + [(k, 99, None)] if _k == k)) if entry["layers"] else None

    # Curated layers with explicit ids first, primary: true on the best-ranked one.
    for layer_key, rank, _ in TARGETS:
        if layer_key not in entry["layers"]:
            continue
        ids = entry["ids"].get(layer_key, [])
        for fid in ids:
            f = {"layer": layer_key, "id": fid}
            if layer_key == primary_layer:
                f["primary"] = True
            feats.append(f)

    # A single hydropower_points match covers all three hydropower layers
    # (they share the alias). Use the EXACT project string to avoid collisions.
    if any(l.startswith("hydropower_") for l in entry["layers"]):
        exact_name = entry["props"].get("project") or entry["name"]
        feats.append({
            "layer": "hydropower_points",
            "match": {"field": "project", "value": exact_name},
            **({"primary": True} if primary_layer and primary_layer.startswith("hydropower_") else {}),
        })
    return feats


def default_layers_on(entry: dict) -> list[str]:
    base = ["country_outline", "tributaries"]
    if any(l.startswith("hydropower_") for l in entry["layers"]):
        lt = (entry["props"].get("license_type") or "").lower()
        if lt == "operation":   base.append("hydropower_operating")
        elif lt == "generation":base.append("hydropower_construction")
        elif lt == "survey":    base.append("hydropower_survey")
    if "storage_shortlist"  in entry["layers"]: base.append("storage_shortlist")
    if "priority_watchlist" in entry["layers"]: base.append("priority_watchlist")
    if "top_capacity"       in entry["layers"]: base.append("top_capacity")
    # dedupe
    seen, out = set(), []
    for x in base:
        if x not in seen: seen.add(x); out.append(x)
    return out


def sync_bindings(bindings: dict, targets: dict[str, dict], existing_slugs: set[str], write: bool) -> dict:
    """Add entries for slugs that have a file but no binding. Returns stats."""
    pages = bindings.setdefault("pages", {})
    stats = {"added": 0, "already": 0, "aliased_added": 0}
    for raw_slug, entry in targets.items():
        slug = SLUG_ALIASES.get(raw_slug, raw_slug)
        if slug not in existing_slugs and raw_slug not in existing_slugs:
            # we're about to create the stub at slug == raw_slug
            slug = raw_slug
        if slug in pages:
            stats["already"] += 1
            continue
        features = build_binding_features(entry, slug)
        if not features:
            continue
        pages[slug] = {
            "type": "entity",
            "category": "project",
            "features": features,
            "layers_on": default_layers_on(entry),
        }
        if raw_slug in SLUG_ALIASES:
            stats["aliased_added"] += 1
        else:
            stats["added"] += 1
    if write:
        BINDINGS.write_text(json.dumps(bindings, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    return stats


